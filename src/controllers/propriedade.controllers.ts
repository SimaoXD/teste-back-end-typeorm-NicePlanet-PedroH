import { Request, Response } from "express";

const createPropriedade = async (req: Request, res: Response): Promise<Response> => {
  const payload: IPropriedadeRegister = res.locals.data;
  const propriedade = await requestCreatePropriedade(payload);

  return res.status(201).json(propriedade);
};

const getPropriedadeList = async (req: Request, res: Response): Promise<Reponse> => {
  const listPropriedade = await requestPropriedadeList();

  return res.status(200).json(listPropriedade);
};

export { createPropriedade, getPropriedadeList };
